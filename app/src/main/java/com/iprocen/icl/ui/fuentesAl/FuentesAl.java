package com.iprocen.icl.ui.fuentesAl;

public class FuentesAl {
    private Long id;
    private String fase;
    private String s_tension;
    private String s_corriente;
    private String clasific;
    private String desc;
    private String nmro_parte;
    private String protect_sobre;
    private String voltaje;
    private String corriente;

    public FuentesAl() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFase() {
        return fase;
    }

    public void setFase(String fase) {
        this.fase = fase;
    }

    public String getS_tension() {
        return s_tension;
    }

    public void setS_tension(String s_tension) {
        this.s_tension = s_tension;
    }

    public String getS_corriente() {
        return s_corriente;
    }

    public void setS_corriente(String s_corriente) {
        this.s_corriente = s_corriente;
    }

    public String getClasific() {
        return clasific;
    }

    public void setClasific(String clasific) {
        this.clasific = clasific;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getNmro_parte() {
        return nmro_parte;
    }

    public void setNmro_parte(String nmro_parte) {
        this.nmro_parte = nmro_parte;
    }

    public String getProtect_sobre() {
        return protect_sobre;
    }

    public void setProtect_sobre(String protect_sobre) {
        this.protect_sobre = protect_sobre;
    }

    public String getVoltaje() {
        return voltaje;
    }

    public void setVoltaje(String voltaje) {
        this.voltaje = voltaje;
    }

    public String getCorriente() {
        return corriente;
    }

    public void setCorriente(String corriente) {
        this.corriente = corriente;
    }

}
