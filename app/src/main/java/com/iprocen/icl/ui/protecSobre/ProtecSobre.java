package com.iprocen.icl.ui.protecSobre;

public class ProtecSobre {
    private Long id;
    private String senial;
    private String conexion;
    private String voltaje;
    private String aterrizaje;
    private String disenio;
    private String monitoreo;
    private String desc;
    private String nmro_parte;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSenial() {
        return senial;
    }

    public void setSenial(String senial) {
        this.senial = senial;
    }

    public String getConexion() {
        return conexion;
    }

    public void setConexion(String conexion) {
        this.conexion = conexion;
    }

    public String getVoltaje() {
        return voltaje;
    }

    public void setVoltaje(String voltaje) {
        this.voltaje = voltaje;
    }

    public String getAterrizaje() {
        return aterrizaje;
    }

    public void setAterrizaje(String aterrizaje) {
        this.aterrizaje = aterrizaje;
    }

    public String getDisenio() {
        return disenio;
    }

    public void setDisenio(String disenio) {
        this.disenio = disenio;
    }

    public String getMonitoreo() {
        return monitoreo;
    }

    public void setMonitoreo(String monitoreo) {
        this.monitoreo = monitoreo;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getNmro_parte() {
        return nmro_parte;
    }

    public void setNmro_parte(String nmro_parte) {
        this.nmro_parte = nmro_parte;
    }

}
