package com.iprocen.icl.ui.borneras;

public class Bornera {
    private Long id;
    private String tipo_conex;
    private String awg;
    private String l_max;
    private String puntos_conex;
    private String tapa;
    private String puente;
    private String destornillador;
    private String etiq_lado;
    private String etiq_sup;
    private String desc;
    private String nmro_parte;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTipo_conex() {
        return tipo_conex;
    }

    public void setTipo_conex(String tipo_conex) {
        this.tipo_conex = tipo_conex;
    }

    public String getAwg() {
        return awg;
    }

    public void setAwg(String awg) {
        this.awg = awg;
    }

    public String getL_max() {
        return l_max;
    }

    public void setL_max(String l_max) {
        this.l_max = l_max;
    }

    public String getPuntos_conex() {
        return puntos_conex;
    }

    public void setPuntos_conex(String puntos_conex) {
        this.puntos_conex = puntos_conex;
    }

    public String getTapa() {
        return tapa;
    }

    public void setTapa(String tapa) {
        this.tapa = tapa;
    }

    public String getPuente() {
        return puente;
    }

    public void setPuente(String puente) {
        this.puente = puente;
    }

    public String getDestornillador() {
        return destornillador;
    }

    public void setDestornillador(String destornillador) {
        this.destornillador = destornillador;
    }

    public String getEtiq_lado() {
        return etiq_lado;
    }

    public void setEtiq_lado(String etiq_lado) {
        this.etiq_lado = etiq_lado;
    }

    public String getEtiq_sup() {
        return etiq_sup;
    }

    public void setEtiq_sup(String etiq_sup) {
        this.etiq_sup = etiq_sup;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getNmro_parte() {
        return nmro_parte;
    }

    public void setNmro_parte(String nmro_parte) {
        this.nmro_parte = nmro_parte;
    }

}
